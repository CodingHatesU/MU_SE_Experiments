//Arithmetic Class

package org.calculator;
public class Arithmetic{
    public int add(int a, int b) { //method to add
        return a+b;
    }

    public int sub(int a, int b) { // method to subtract
        return a-b;
    }

    public int multi(int a, int b) { //method to multiply
        return a*b;
    }

    public int div(int a, int b) { //method to divide
        return a/b;
    }

}
